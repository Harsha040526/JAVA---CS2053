class Student {
    String name;
    int age;

    void displayInfo() {
        System.out.println("Name: " + name + " Age: " + age);
    }

    public static void main(String[] args) {
        Student s1 = new Student();
        s1.name = "Harsha M";
        s1.age = 19;
        s1.displayInfo();
    }
}
